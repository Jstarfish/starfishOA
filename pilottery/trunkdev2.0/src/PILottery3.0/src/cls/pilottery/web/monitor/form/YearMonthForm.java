package cls.pilottery.web.monitor.form;

import java.io.Serializable;

public class YearMonthForm implements Serializable {
	private static final long serialVersionUID = -2787155021515354327L;

	private String year;

	private String month;

	private String insCode;

	private String tjType;

	public String getInsCode() {
		return insCode;
	}

	public void setInsCode(String insCode) {
		this.insCode = insCode;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getTjType() {
		return tjType;
	}

	public void setTjType(String tjType) {
		this.tjType = tjType;
	}

	public YearMonthForm() {
	}

	public YearMonthForm(String year, String insCode,String month, String tjType) {
		this.year = year;
		this.month = month;
		this.insCode = insCode;
		this.tjType = tjType;
	}
	
	

}
