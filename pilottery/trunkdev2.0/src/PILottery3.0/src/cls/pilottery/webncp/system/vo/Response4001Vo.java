package cls.pilottery.webncp.system.vo;

import java.io.Serializable;

public class Response4001Vo implements Serializable {

	private static final long serialVersionUID = 1818980717242932392L;
	private String gameCode;
	private String perdIssue;
	private String drawCode;
	private String drawTime;
	
	public Response4001Vo(){}
	
	
	public Response4001Vo(String gameCode, String perdIssue, String drawCode,
			String drawTime) {
		super();
		this.gameCode = gameCode;
		this.perdIssue = perdIssue;
		this.drawCode = drawCode;
		this.drawTime = drawTime;
	}


	public String getGameCode() {
		return gameCode;
	}
	public void setGameCode(String gameCode) {
		this.gameCode = gameCode;
	}
	
	public String getPerdIssue() {
		return perdIssue;
	}
	public void setPerdIssue(String perdIssue) {
		this.perdIssue = perdIssue;
	}
	
	public String getDrawCode() {
		return drawCode;
	}
	public void setDrawCode(String drawCode) {
		this.drawCode = drawCode;
	}
	
	public String getDrawTime() {
		return drawTime;
	}
	public void setDrawTime(String drawTime) {
		this.drawTime = drawTime;
	}
}
