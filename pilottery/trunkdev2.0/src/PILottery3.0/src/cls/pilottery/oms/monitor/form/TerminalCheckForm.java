package cls.pilottery.oms.monitor.form;

import cls.pilottery.common.model.BaseEntity;

public class TerminalCheckForm extends BaseEntity {
	private static final long serialVersionUID = -972291214970002195L;

	private String beginDate;
	private String endDate;

	private int termCheckId; // TERM_CHECK_ID 巡检ID
	private String termCheckName; // 巡检员姓名
	private String agencyCode;
	private String agencyName;
	private String terminalCode;
	private String orgCode; // 站点所属部门

	private String cuserOrg; // 当前用户所属部门

	public String getOrgCode() {
		return orgCode;
	}

	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}

	public String getCuserOrg() {
		return cuserOrg;
	}

	public void setCuserOrg(String cuserOrg) {
		this.cuserOrg = cuserOrg;
	}

	public String getBeginDate() {
		return beginDate;
	}

	public void setBeginDate(String beginDate) {
		this.beginDate = beginDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public int getTermCheckId() {
		return termCheckId;
	}

	public void setTermCheckId(int termCheckId) {
		this.termCheckId = termCheckId;
	}

	public String getTermCheckName() {
		return termCheckName;
	}

	public void setTermCheckName(String termCheckName) {
		this.termCheckName = termCheckName;
	}

	public String getAgencyCode() {
		return agencyCode;
	}

	public void setAgencyCode(String agencyCode) {
		this.agencyCode = agencyCode;
	}

	public String getAgencyName() {
		return agencyName;
	}

	public void setAgencyName(String agencyName) {
		this.agencyName = agencyName;
	}

	public String getTerminalCode() {
		return terminalCode;
	}

	public void setTerminalCode(String terminalCode) {
		this.terminalCode = terminalCode;
	}

}
