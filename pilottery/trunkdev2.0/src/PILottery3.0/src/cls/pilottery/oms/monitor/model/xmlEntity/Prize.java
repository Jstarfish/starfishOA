package cls.pilottery.oms.monitor.model.xmlEntity;

public class Prize {

	private String prize_level; // 奖级
	private String prize_name; // 奖级名称
	private String prize_num; // 中奖注数
	private String prize_amount; //

	public String getPrize_level() {
		return prize_level;
	}

	public void setPrize_level(String prizeLevel) {
		prize_level = prizeLevel;
	}

	public String getPrize_name() {
		return prize_name;
	}

	public void setPrize_name(String prizeName) {
		prize_name = prizeName;
	}

	public String getPrize_num() {
		return prize_num;
	}

	public void setPrize_num(String prizeNum) {
		prize_num = prizeNum;
	}

	public String getPrize_amount() {
		return prize_amount;
	}

	public void setPrize_amount(String prizeAmount) {
		prize_amount = prizeAmount;
	}

}
