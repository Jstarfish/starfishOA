package cls.pilottery.oms.monitor.model;

import cls.pilottery.common.model.BaseEntity;

public class Agency extends BaseEntity {

	private static final long serialVersionUID = 4874891317669777992L;
	private String agencyName; // AGENCY_NAME 销售站名称
	private String areaCode; // AREA_CODE 区域ID
	private String contactPerson; // CONTACT_PERSON 销售站联系人
	private String address; // ADDRESS 销售站地址
	private int agencyType; // AGENCY_TYPE
							// 销售站类型（1=传统终端(预付费)；2=受信终端(后付费)；3=无纸化；4=中心销售站）

	/* 刷新字段 */
	private String statusText;
	private String resetText;
	private String agencyTypeText;
	private String agencyCodeText;
	// 显示完整区域
	private String areaText;

	public String getAgencyName() {
		return agencyName;
	}

	public void setAgencyName(String agencyName) {
		this.agencyName = agencyName;
	}

	public String getAreaCode() {
		return areaCode;
	}

	public void setAreaCode(String areaCode) {
		this.areaCode = areaCode;
	}

	public String getContactPerson() {
		return contactPerson;
	}

	public void setContactPerson(String contactPerson) {
		this.contactPerson = contactPerson;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public int getAgencyType() {
		return agencyType;
	}

	public void setAgencyType(int agencyType) {
		this.agencyType = agencyType;
	}

	public String getStatusText() {
		return statusText;
	}

	public void setStatusText(String statusText) {
		this.statusText = statusText;
	}

	public String getResetText() {
		return resetText;
	}

	public void setResetText(String resetText) {
		this.resetText = resetText;
	}

	public String getAgencyTypeText() {
		return agencyTypeText;
	}

	public void setAgencyTypeText(String agencyTypeText) {
		this.agencyTypeText = agencyTypeText;
	}

	public String getAgencyCodeText() {
		return agencyCodeText;
	}

	public void setAgencyCodeText(String agencyCodeText) {
		this.agencyCodeText = agencyCodeText;
	}

	public String getAreaText() {
		return areaText;
	}

	public void setAreaText(String areaText) {
		this.areaText = areaText;
	}
}
