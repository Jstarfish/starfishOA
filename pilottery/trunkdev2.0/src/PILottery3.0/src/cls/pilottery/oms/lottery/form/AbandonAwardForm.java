package cls.pilottery.oms.lottery.form;

import java.util.Date;

import cls.pilottery.oms.lottery.model.AbandonAward;

public class AbandonAwardForm {

	private AbandonAward abandonAward;

	public AbandonAward getInstance() {
		if (abandonAward == null)
			abandonAward = new AbandonAward();

		return abandonAward;
	}

	private int issueNumber;

	private Date payDate;

	public int getIssueNumber() {
		return issueNumber;
	}

	public void setIssueNumber(int issueNumber) {
		this.issueNumber = issueNumber;
	}

	public Date getPayDate() {
		return payDate;
	}

	public void setPayDate(Date payDate) {
		this.payDate = payDate;
	}
}
