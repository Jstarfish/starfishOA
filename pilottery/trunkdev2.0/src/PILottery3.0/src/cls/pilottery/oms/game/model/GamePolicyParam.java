package cls.pilottery.oms.game.model;

import java.util.Date;

import cls.pilottery.common.model.BaseEntity;
/**
 *  游戏政策参数 (GP_POLICY)
 *  
 *  @author Woo
 */
public class GamePolicyParam extends BaseEntity {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 8186542701624732979L;
	
	private Long hisPolicyCode;	//历史编号
	private Date hisModifyDate;	//修改时间
	private Short gameCode;		//游戏编码
	private int theoryRate;		//THEORY_RATE		理论返奖率
	private int fundRate;		//FUND_RATE			公益金比例
	private int adjRate;		//ADJ_RATE			调解基金比例
	private Long taxThreshold;	//TAX_THRESHOLD		中奖缴税起征点
	private int taxRate;		//TAX_RATE			中奖缴税比例
	private int drawLimitDay;	//DRAW_LIMIT_DAY	兑奖期
	
	public Long getHisPolicyCode() {
		return hisPolicyCode;
	}
	public void setHisPolicyCode(Long hisPolicyCode) {
		this.hisPolicyCode = hisPolicyCode;
	}
	public Date getHisModifyDate() {
		return hisModifyDate;
	}
	public void setHisModifyDate(Date hisModifyDate) {
		this.hisModifyDate = hisModifyDate;
	}
	public Short getGameCode() {
		return gameCode;
	}
	public void setGameCode(Short gameCode) {
		this.gameCode = gameCode;
	}
	public int getTheoryRate() {
		return theoryRate;
	}
	public void setTheoryRate(int theoryRate) {
		this.theoryRate = theoryRate;
	}
	public int getFundRate() {
		return fundRate;
	}
	public void setFundRate(int fundRate) {
		this.fundRate = fundRate;
	}
	public int getAdjRate() {
		return adjRate;
	}
	public void setAdjRate(int adjRate) {
		this.adjRate = adjRate;
	}
	public Long getTaxThreshold() {
		return taxThreshold;
	}
	public void setTaxThreshold(Long taxThreshold) {
		this.taxThreshold = taxThreshold;
	}
	public int getTaxRate() {
		return taxRate;
	}
	public void setTaxRate(int taxRate) {
		this.taxRate = taxRate;
	}
	public int getDrawLimitDay() {
		return drawLimitDay;
	}
	public void setDrawLimitDay(int drawLimitDay) {
		this.drawLimitDay = drawLimitDay;
	}
}
