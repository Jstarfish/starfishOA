	regex4:/^[A-Za-z0-9\u4e00-\u9fa5]*$/,   //过滤特殊字符
	regex5:/^((?:(?:25[0-5]|2[0-4]\d|((1\d{2})|( [1-9]?\d)))\.){3}(?:25[0-5]|2[0-4]\d|((1\d{2})|([1-9]?\d))))?$/,//IP过滤
	regex6:/^(\/[1-9]{3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\:[0-9]{3,5})?$/  //IP过滤
