package cls.pilottery.oms.business.model;

public class ShopType implements java.io.Serializable{
	
	private static final long serialVersionUID = 5211347776022833827L;

	private Integer value;
	private String  name;
	
	public ShopType(){
		
	}
	
	public ShopType(Integer v,String n){
		value = v;
		name = n;
	}
	
	public Integer getValue() {
		return value;
	}
	
	public void setValue(Integer value) {
		this.value = value;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
}
