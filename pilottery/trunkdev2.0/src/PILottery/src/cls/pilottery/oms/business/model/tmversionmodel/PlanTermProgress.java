package cls.pilottery.oms.business.model.tmversionmodel;

public class PlanTermProgress {

	Integer planId;
	String termCode;
	String termCodeTochar;
	String pkgVer;

	String startDate;
	String curFile;
	String curFileProgress;

	Integer isComplete;
	String endDate;

	public String getIsCompleteStr() {
		return isComplete > 0 ? "Y" : "N";
	}

	// //////////////////////////////////////////
	public String getTermCode() {
		return termCode;
	}

	public void setTermCode(String termCode) {
		this.termCode = termCode;
	}

	public String getPkgVer() {
		return pkgVer;
	}

	public void setPkgVer(String pkgVer) {
		this.pkgVer = pkgVer;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getCurFile() {
		return curFile;
	}

	public void setCurFile(String curFile) {
		this.curFile = curFile;
	}

	public String getCurFileProgress() {
		return curFileProgress;
	}

	public void setCurFileProgress(String curFileProgress) {
		this.curFileProgress = curFileProgress;
	}

	public Integer getIsComplete() {
		return isComplete;
	}

	public void setIsComplete(Integer isComplete) {
		this.isComplete = isComplete;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public Integer getPlanId() {
		return planId;
	}

	public void setPlanId(Integer planId) {
		this.planId = planId;
	}

	public String getTermCodeTochar() {
		return termCodeTochar;
	}

	public void setTermCodeTochar(String termCodeTochar) {
		this.termCodeTochar = termCodeTochar;
	}

}
