package cls.pilottery.oms.lottery.model;

import java.util.Date;

import cls.pilottery.common.model.BaseEntity;

public class ReconciliationVo extends BaseEntity {
	/**
	 * 
	 */
	private static final long serialVersionUID = -8191866514268219744L;
	private Long id;
	private String agencyCode;
	private String agencyName;
	private Long operAmount;
	private Date operTime;
	private Long state;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getAgencyCode() {
		return agencyCode;
	}

	public void setAgencyCode(String agencyCode) {
		this.agencyCode = agencyCode;
	}

	public String getAgencyName() {
		return agencyName;
	}

	public void setAgencyName(String agencyName) {
		this.agencyName = agencyName;
	}

	public Long getOperAmount() {
		return operAmount;
	}

	public void setOperAmount(Long operAmount) {
		this.operAmount = operAmount;
	}

	public Date getOperTime() {
		return operTime;
	}

	public void setOperTime(Date operTime) {
		this.operTime = operTime;
	}

	public Long getState() {
		return state;
	}

	public void setState(Long state) {
		this.state = state;
	}

}
