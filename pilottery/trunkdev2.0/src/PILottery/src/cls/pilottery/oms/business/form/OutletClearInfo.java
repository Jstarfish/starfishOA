package cls.pilottery.oms.business.form;

import cls.pilottery.common.entity.AbstractEntity;

public class OutletClearInfo extends AbstractEntity {
	private static final long serialVersionUID = 755955783290407870L;
	private String agencyCode;
	private Integer userId;
	private Long balance;
	public String getAgencyCode() {
		return agencyCode;
	}
	public void setAgencyCode(String agencyCode) {
		this.agencyCode = agencyCode;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public Long getBalance() {
		return balance;
	}
	public void setBalance(Long balance) {
		this.balance = balance;
	}

}
