package cls.pilottery.oms.lottery.model;

import cls.pilottery.common.model.BaseEntity;

public class Betinfo extends BaseEntity {
    private String play;// 玩法
    private String bet;// 投注方式
    private String bettingnumber;// 投注号码
    private String multiple;// 倍数
    private String extendedparameters;// 扩展字段
    private long lineAmount;

    public long getLineAmount() {
        return lineAmount;
    }

    public void setLineAmount(long lineAmount) {
        this.lineAmount = lineAmount;
    }

    public String getPlay() {
        return play;
    }

    public void setPlay(String play) {
        this.play = play;
    }

    public String getBet() {
        return bet;
    }

    public void setBet(String bet) {
        this.bet = bet;
    }

    public String getMultiple() {
        return multiple;
    }

    public void setMultiple(String multiple) {
        this.multiple = multiple;
    }

    public String getExtendedparameters() {
        return extendedparameters;
    }

    public void setExtendedparameters(String extendedparameters) {
        this.extendedparameters = extendedparameters;
    }

    public String getBettingnumber() {
        return bettingnumber;
    }

    public void setBettingnumber(String bettingnumber) {
        this.bettingnumber = bettingnumber;
    }

}
