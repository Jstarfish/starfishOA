package cls.pilottery.web.report.model;

import java.io.Serializable;

public class ManagerFundReportVO implements Serializable{
	private static final long serialVersionUID = -5750883246914211725L;

	private String calcDate;

	private String marketAdmin;

	private long topUp;

	private long paid; // 现金上缴

	private long withdrawn;

	private long initialBalance;

	private long finalBalance;

	private String marketName;
	
	public String getMarketName() {
		return marketName;
	}

	public void setMarketName(String marketName) {
		this.marketName = marketName;
	}

	public String getCalcDate() {
		return calcDate;
	}

	public void setCalcDate(String calcDate) {
		this.calcDate = calcDate;
	}

	public String getMarketAdmin() {
		return marketAdmin;
	}

	public void setMarketAdmin(String marketAdmin) {
		this.marketAdmin = marketAdmin;
	}

	public long getTopUp() {
		return topUp;
	}

	public void setTopUp(long topUp) {
		this.topUp = topUp;
	}

	public long getPaid() {
		return paid;
	}

	public void setPaid(long paid) {
		this.paid = paid;
	}

	public long getWithdrawn() {
		return withdrawn;
	}

	public void setWithdrawn(long withdrawn) {
		this.withdrawn = withdrawn;
	}

	public long getInitialBalance() {
		return initialBalance;
	}

	public void setInitialBalance(long initialBalance) {
		this.initialBalance = initialBalance;
	}

	public long getFinalBalance() {
		return finalBalance;
	}

	public void setFinalBalance(long finalBalance) {
		this.finalBalance = finalBalance;
	}

}
