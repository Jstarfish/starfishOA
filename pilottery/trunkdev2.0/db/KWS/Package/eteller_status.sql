CREATE OR REPLACE PACKAGE eteller_status IS
   /****** 销售员状态（1=可用；2=已禁用；3=已删除） ******/
   enabled                 /* 1-可用 */                    CONSTANT NUMBER := 1;
   disabled                /* 2-已禁用 */                  CONSTANT NUMBER := 2;
   deleted                 /* 3-已删除 */                  CONSTANT NUMBER := 3;
END;
