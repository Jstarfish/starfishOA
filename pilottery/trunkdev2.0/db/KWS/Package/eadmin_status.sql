CREATE OR REPLACE PACKAGE eadmin_status IS
   /****** 用户状态(1-可用，2-删除，3-由于密码原因停用) ******/
   AVAILIBLE                 /* 1=可用 */                  CONSTANT NUMBER := 1;
   DELETED                   /* 2=删除 */                  CONSTANT NUMBER := 2;
   PASSERROR                 /* 3=由于密码原因停用 */      CONSTANT NUMBER := 3;
END;
