package cls.pilottery.web.marketManager.form;

import cls.pilottery.common.model.BaseEntity;

public class MMTransferRecordForm extends BaseEntity {
	private static final long serialVersionUID = 6501011000287112920L;
	private String date;
	private int dealType;
	private String outletCode;
	private String outletName;
	private double amount;
	private int marketManagerId;
	private String marketManagerName;
	private int currentUserId;
	private String cuserOrg; // 当前用户所属部门
	private String no;   //编码
	
	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public int getMarketManagerId() {
		return marketManagerId;
	}

	public void setMarketManagerId(int marketManagerId) {
		this.marketManagerId = marketManagerId;
	}

	public String getCuserOrg() {
		return cuserOrg;
	}

	public void setCuserOrg(String cuserOrg) {
		this.cuserOrg = cuserOrg;
	}

	public int getCurrentUserId() {
		return currentUserId;
	}

	public void setCurrentUserId(int currentUserId) {
		this.currentUserId = currentUserId;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public int getDealType() {
		return dealType;
	}

	public void setDealType(int dealType) {
		this.dealType = dealType;
	}

	public String getOutletCode() {
		return outletCode;
	}

	public void setOutletCode(String outletCode) {
		this.outletCode = outletCode;
	}

	public String getOutletName() {
		return outletName;
	}

	public void setOutletName(String outletName) {
		this.outletName = outletName;
	}

	public double getAmount() {
		return amount;
	}

	public void setAmount(double amount) {
		this.amount = amount;
	}

	public String getMarketManagerName() {
		return marketManagerName;
	}

	public void setMarketManagerName(String marketManagerName) {
		this.marketManagerName = marketManagerName;
	}

}
