package cls.pilottery.oms.monitor.form;

import cls.pilottery.common.model.BaseEntity;

public class EventsForm extends BaseEntity {

	private static final long serialVersionUID = -2771383577060553961L;
	private String eventLevel;
	private String eventType;
	private String serverAddres;

	private String beginDate;
	private String endDate;


	public String getEventLevel() {
		return eventLevel;
	}

	public void setEventLevel(String eventLevel) {
		this.eventLevel = eventLevel;
	}

	public String getEventType() {
		return eventType;
	}

	public void setEventType(String eventType) {
		this.eventType = eventType;
	}

	public String getServerAddres() {
		return serverAddres;
	}

	public void setServerAddres(String serverAddres) {
		this.serverAddres = serverAddres;
	}

	public String getBeginDate() {
		return beginDate;
	}

	public void setBeginDate(String beginDate) {
		this.beginDate = beginDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

}
