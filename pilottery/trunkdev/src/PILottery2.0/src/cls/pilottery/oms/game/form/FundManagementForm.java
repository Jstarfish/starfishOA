package cls.pilottery.oms.game.form;

import cls.pilottery.oms.game.model.FundAdj;
import cls.pilottery.oms.game.model.FundAdjHistoryVo;
import cls.pilottery.oms.game.model.GovernmentCommision;

/**
 * 游戏管理，调节基金管理Form
 * 
 * @author Woo
 */
public class FundManagementForm {
	
	private FundAdj	fundAdj;
	private FundAdjHistoryVo fundAdjHistoryVo;
	private GovernmentCommision governmentCommision;
	private String str_adjTime1;
	private String str_adjTime2;
	private Long beginIssue;
	
	public Long getBeginIssue() {
		return beginIssue;
	}

	public void setBeginIssue(Long beginIssue) {
		this.beginIssue = beginIssue;
	}

	public GovernmentCommision getGovernmentCommision() {
		if(governmentCommision == null)
			governmentCommision = new GovernmentCommision();
		return governmentCommision;
	}

	public void setGovernmentCommision(GovernmentCommision governmentCommision) {
		this.governmentCommision = governmentCommision;
	}

	public FundAdjHistoryVo getFundAdjHistoryVo() {
		if(fundAdjHistoryVo == null)
			fundAdjHistoryVo = new FundAdjHistoryVo();
		return fundAdjHistoryVo;
	}

	public void setFundAdjHistoryVo(FundAdjHistoryVo fundAdjHistoryVo) {
		this.fundAdjHistoryVo = fundAdjHistoryVo;
	}

	public FundAdj getFundAdj() {
		if(fundAdj == null)
			fundAdj = new FundAdj();
		return fundAdj;
	}

	public void setFundAdj(FundAdj fundAdj) {
		this.fundAdj = fundAdj;
	}

	public String getStr_adjTime1() {
		return str_adjTime1;
	}

	public void setStr_adjTime1(String str_adjTime1) {
		this.str_adjTime1 = str_adjTime1;
	}

	public String getStr_adjTime2() {
		return str_adjTime2;
	}

	public void setStr_adjTime2(String str_adjTime2) {
		this.str_adjTime2 = str_adjTime2;
	}

}
