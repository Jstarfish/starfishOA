package cls.pilottery.oms.issue.model;

import java.util.ArrayList;
import java.util.List;

public class GameDrawXml {

    private String draw_result;
    private String sale_total_amount;
    private String pool_left_amount;
    private List<Prize> prizes = new ArrayList<Prize>();
    private List<HighPrize> highPrizes = new ArrayList<HighPrize>();

    public String getDraw_result() {
        return draw_result;
    }
    public void setDraw_result(String draw_result) {
        this.draw_result = draw_result;
    }

    public String getSale_total_amount() {
        return sale_total_amount;
    }
    public void setSale_total_amount(String sale_total_amount) {
        this.sale_total_amount = sale_total_amount;
    }

    public String getPool_left_amount() {
        return pool_left_amount;
    }
    public void setPool_left_amount(String pool_left_amount) {
        this.pool_left_amount = pool_left_amount;
    }

    public List<Prize> getPrizes() {
        return prizes;
    }
    public void setPrizes(List<Prize> prizes) {
        this.prizes = prizes;
    }
    
	public List<HighPrize> getHighPrizes() {
		return highPrizes;
	}
	public void setHighPrizes(List<HighPrize> highPrizes) {
		this.highPrizes = highPrizes;
	}

}
