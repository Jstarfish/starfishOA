package cls.pilottery.oms.monitor.model;

import cls.pilottery.oms.monitor.model.xmlEntity.HightPrize;
import cls.pilottery.oms.monitor.model.xmlEntity.Prize;

public class GameIssueDetail {
	private long gameCode; // GAME_CODE 游戏编码
	private long issueNumber; // ISSUE_NUMBER 期次编码
	private String xmlContent; // XML_CONTENT 开奖公告

	/* xml文件信息 */

	private Prize[] prizes;
	private HightPrize[] high_prizes;

	public long getGameCode() {
		return gameCode;
	}

	public void setGameCode(long gameCode) {
		this.gameCode = gameCode;
	}

	public long getIssueNumber() {
		return issueNumber;
	}

	public void setIssueNumber(long issueNumber) {
		this.issueNumber = issueNumber;
	}

	public String getXmlContent() {
		return xmlContent;
	}

	public void setXmlContent(String xmlContent) {
		this.xmlContent = xmlContent;
	}

	public Prize[] getPrizes() {
		return prizes;
	}

	public void setPrizes(Prize[] prizes) {
		this.prizes = prizes;
	}

	public HightPrize[] getHigh_prizes() {
		return high_prizes;
	}

	public void setHigh_prizes(HightPrize[] highPrizes) {
		high_prizes = highPrizes;
	}

}
